# 기본 : 숫자 (정수형, 실수형)
a=10
b=-22
print(a, type(a))
print(b, type(b))

# 연산 - 사칙연산
su=10
value=3
hap=su+value
sub=su-value
mul=su*value
div=su/value                     # 실수형  3.3333~~~      
n=su//value                      # 정수형 몫 3

print(hap, type(hap))
print(sub, type(sub))
print(mul, type(mul))
print(div, type(div))
print(n, type(n))
print("\n\n")

# 실수형
num=3.14
print(num, type(num))

a=num+su
b=num-su
print(a, type(a))
print(b, type(b))

